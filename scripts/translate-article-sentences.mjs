import { writeFileSync } from "node:fs";

import { articles } from "../src/articles.js";

const targetFile = new URL("../src/articles.js", import.meta.url);
const delimiter = "§§IELTS_SPLIT_7F3A§§";
const maxBatchItems = 24;
const maxBatchChars = 3600;
const saveEveryBatches = 8;

function normalizeSentence(value) {
  return (value || "").replace(/\s+/g, " ").trim();
}

function needsTranslation(sentence) {
  return (
    !sentence.translation ||
    /待生成|段落释义|句子释义|暂无精确句子释义|正在生成/.test(sentence.translation)
  );
}

function collectPendingSentences() {
  const pending = [];

  articles.forEach((article, articleIndex) => {
    article.paragraphs.forEach((paragraph, paragraphIndex) => {
      paragraph.forEach((sentence, sentenceIndex) => {
        if (needsTranslation(sentence)) {
          pending.push({
            articleIndex,
            paragraphIndex,
            sentenceIndex,
            text: normalizeSentence(sentence.text),
          });
        }
      });
    });
  });

  return pending;
}

function createBatches(items) {
  const batches = [];
  let batch = [];
  let charCount = 0;

  items.forEach((item) => {
    const nextChars = item.text.length + delimiter.length + 2;
    const shouldStartNewBatch =
      batch.length &&
      (batch.length >= maxBatchItems || charCount + nextChars > maxBatchChars);

    if (shouldStartNewBatch) {
      batches.push(batch);
      batch = [];
      charCount = 0;
    }

    batch.push(item);
    charCount += nextChars;
  });

  if (batch.length) {
    batches.push(batch);
  }

  return batches;
}

async function translateBatch(texts) {
  const endpoint = new URL("https://translate.googleapis.com/translate_a/single");
  endpoint.searchParams.set("client", "gtx");
  endpoint.searchParams.set("sl", "en");
  endpoint.searchParams.set("tl", "zh-CN");
  endpoint.searchParams.set("dt", "t");
  endpoint.searchParams.set("q", texts.join(`\n${delimiter}\n`));

  const response = await fetch(endpoint.toString());
  if (!response.ok) {
    throw new Error(`translation request failed: ${response.status}`);
  }

  const data = await response.json();
  const translated = (data?.[0] || [])
    .map((part) => part?.[0] || "")
    .join("")
    .trim();
  const parts = translated
    .split(delimiter)
    .map((part) => part.replace(/\s+/g, " ").trim())
    .filter(Boolean);

  if (parts.length === texts.length) {
    return parts;
  }

  if (texts.length === 1 && translated) {
    return [translated.replace(/\s+/g, " ").trim()];
  }

  const midpoint = Math.ceil(texts.length / 2);
  return [
    ...(await translateBatch(texts.slice(0, midpoint))),
    ...(await translateBatch(texts.slice(midpoint))),
  ];
}

function applyTranslations(batch, translations) {
  batch.forEach((item, index) => {
    articles[item.articleIndex].paragraphs[item.paragraphIndex][item.sentenceIndex].translation =
      translations[index] || "本句中文释义待生成。";
  });
}

function saveArticles() {
  const output = `// Generated by scripts/import-reading-articles.mjs from local reading data.
// Sentence translations are generated by scripts/translate-article-sentences.mjs.
// Do not edit this file by hand when refreshing imported passages.

export const articles = ${JSON.stringify(articles, null, 2)};
`;

  writeFileSync(targetFile, output, "utf8");
}

async function main() {
  const pending = collectPendingSentences();
  const batches = createBatches(pending);
  let translatedCount = 0;

  console.log(
    JSON.stringify(
      {
        pending: pending.length,
        batches: batches.length,
        maxBatchItems,
        maxBatchChars,
      },
      null,
      2,
    ),
  );

  for (const [batchIndex, batch] of batches.entries()) {
    const translations = await translateBatch(batch.map((item) => item.text));
    applyTranslations(batch, translations);
    translatedCount += batch.length;

    if ((batchIndex + 1) % saveEveryBatches === 0 || batchIndex === batches.length - 1) {
      saveArticles();
    }

    console.log(
      JSON.stringify({
        batch: batchIndex + 1,
        batches: batches.length,
        translatedCount,
      }),
    );
  }

  saveArticles();
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
